    private void ucitajTim() {
        DefaultComboBoxModel<Tim> tm = new DefaultComboBoxModel<>();
        Tim tim = new Tim();
        tim.setSifraTima(Integer.valueOf(0));
        tim.setNazivTima("Djelatnik bez tima");
        tm.addElement(tim);
        if (cbTim1.getSelectedItem() != null || cbTim1.getSelectedIndex() != 0) {
            new ObradaTim().read().forEach(s -> {
                if (!s.equals(cbTim1.getSelectedItem())) {
                    tm.addElement(s);
                }
            });
        } else {
            new ObradaTim().read().forEach(s -> {
                tm.addElement(s);
            });
        }

        cbTim.setModel(tm);
    }

