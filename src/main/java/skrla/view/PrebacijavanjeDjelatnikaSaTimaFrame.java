    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultListModel<Djelatnik> m = (DefaultListModel<Djelatnik>) lstDjelatnik1.getModel();
        if (cbTim.getSelectedIndex() != 0) {
            obradaTim.setEntitet((Tim) cbTim.getSelectedItem());
            var e = obradaTim.getEntitet();
            for (Djelatnik d : lstDjelatnik1.getSelectedValuesList()) {
                m.removeElement(d);
                d.setTim(e);
                obradaDjelatnik.setEntitet(d);
                spremi();
            }
        } else {
            for (Djelatnik d : lstDjelatnik1.getSelectedValuesList()) {
                m.removeElement(d);
                d.setPoslovnaJedinica(null);
                obradaDjelatnik.setEntitet(d);
                spremi();
            }
        }
        ucitajDjelatnikeNaTimu((Tim) cbTim.getSelectedItem());
        ucitajDjelatnikeNaTimu1((Tim) cbTim1.getSelectedItem());
    }//GEN-LAST:event_jButton1ActionPerformed

