    private void ucitajPoslovneJedinice() {
        DefaultComboBoxModel<PoslovnaJedinica> ms = new DefaultComboBoxModel<>();
        PoslovnaJedinica poslovnaJedinica = new PoslovnaJedinica();
        poslovnaJedinica.setSifraPoslovneJedinice(0);
        poslovnaJedinica.setNazivPoslovneJedinice("Nije odabrano");
        ms.addElement(poslovnaJedinica);
        new ObradaPoslovnaJedinica().read().forEach(s -> {
            ms.addElement(s);
        });
        cbPoslovnaJedinica.setModel(ms);
    }

    private void ucitajTimove() {
        DefaultComboBoxModel<Tim> ms = new DefaultComboBoxModel<>();
        Tim tim = new Tim();
        tim.setSifraTima(0);
        tim.setNazivTima("Nije odabrano");
        ms.addElement(tim);
        new ObradaTim().read().forEach(s -> {
            ms.addElement(s);
        });
        cbTim.setModel(ms);
    }

    private void ucitajDjelatnike() {
        DefaultListModel<Djelatnik> m = new DefaultListModel<>();
        List<Djelatnik> entiteti;
        entiteti = obradaDjelatnik.read(jTxtDjelatnik.getText().trim());

        Collections.sort(entiteti, new ViewUtil());

        for (Djelatnik s : entiteti) {
            m.addElement(s);
        }
        lstDjelatnik.setModel(m);
    }

