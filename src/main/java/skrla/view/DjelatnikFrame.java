    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        if (obradaDjelatnik.getEntitet() == null) {
            JOptionPane.showMessageDialog(getRootPane(), "Prvo odaberite stavku");
            return;
        }
        if (JOptionPane.showConfirmDialog(
                getRootPane(),
                "Sigurno obrisati \"" + obradaDjelatnik.getEntitet().toString() + "\"?",
                "Brisanje",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE) == JOptionPane.NO_OPTION) {
            return;
        }

        try {
            obradaDjelatnik.getEntitet().setAktivan(false);
            obradaDjelatnik.update();
            ucitajDjelatnike();
        } catch (OrganizacijaException ex) {
            JOptionPane.showMessageDialog(getRootPane(), ex.getPoruka());
        }
        lstDjelatnik.repaint();
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void lstDjelatnikValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstDjelatnikValueChanged
        if (evt.getValueIsAdjusting() || lstDjelatnik.getSelectedValue() == null) {
            return;
        }
        obradaDjelatnik.setEntitet(lstDjelatnik.getSelectedValue());
        var e = obradaDjelatnik.getEntitet();

        if (e.getTim() == null) {
            cbTim.setSelectedIndex(0);
        } else {
            cbTim.setSelectedItem(e.getTim());
        }
        if (e.getPoslovnaJedinica() == null) {
            cbPoslovnaJedinica.setSelectedIndex(0);
        } else {
            cbPoslovnaJedinica.setSelectedItem(e.getPoslovnaJedinica());
        }
    }//GEN-LAST:event_lstDjelatnikValueChanged

    private void btnPoslovnaJedinicaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPoslovnaJedinicaActionPerformed
        if (obradaDjelatnik.getEntitet() == null || cbPoslovnaJedinica.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(getRootPane(), "Prvo odaberite poslovnu jedinicu");
            return;
        }
        obradaDjelatnik.getEntitet().setPoslovnaJedinica((PoslovnaJedinica) cbPoslovnaJedinica.getSelectedItem());
        JOptionPane.showMessageDialog(rootPane, "Poslovna jedinica djelatnika uspiješno promjenjena!");
    }//GEN-LAST:event_btnPoslovnaJedinicaActionPerformed

